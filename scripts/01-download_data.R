# Instructions for Downloading the Data: Since downloading the data requires signing in, please follow the provided instructions:
# 1. Visit the American Economic Association journal page at [AEAJournals](https://www.aeaweb.org/journals).
# 2. Search for "Birth rates" in the search bar.
# 3. Click on the journal article titled "The Puzzle of Falling US Birth Rates since the Great Recession".
# 4. Scroll down to the "Additional Materials" section and click on "Replication Package".
# 5. You will be directed to the [OPENICPSR project page](https://www.openicpsr.org/openicpsr/project/144981/version/V1/view).
# 6. Click on "download this package" to proceed.
# 7. Sign in with your OPENICPSR account credentials.
# 8. Once signed in, you will be able to download the project.
